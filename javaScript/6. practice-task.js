/************************************
 * Qs1. print all even number from 0 to 100. *************************************/

// print even number
for (let num = 0; num <= 100; num++) {
    if (num % 2 === 0) {
        console.log("even num =", num);
    }
}


// print ODD number
for (let num = 0; num <= 100; num++) {
    if (num % 2 !== 0) {
        console.log("odd num =", num);
    }
}

